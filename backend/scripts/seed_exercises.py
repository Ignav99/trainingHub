def get_supabase():
    if not SUPABASE_URL or not SUPABASE_KEY:
        print("ERROR: Set SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY env vars")
        sys.exit(1)
    return create_client(SUPABASE_URL, SUPABASE_KEY)


def build_categoria_map(supabase) -> dict:
    """Build a map of category code -> category ID."""
    result = supabase.table("categorias_tarea").select("id, codigo").execute()
    return {r["codigo"]: r["id"] for r in result.data}


