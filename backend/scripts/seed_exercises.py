def seed():
    supabase = get_supabase()

    # Idempotency check: if any seed exercise already exists, skip
    existing = supabase.table("tareas").select("id").eq("es_plantilla", True).eq("titulo", "Rondo 4v2 Clasico").limit(1).execute()
    if existing.data:
        print(f"Seed exercises already exist. Skipping.")
        return

    # Get a valid organizacion_id (required NOT NULL column)
    org_result = supabase.table("organizaciones").select("id").limit(1).execute()
    if not org_result.data:
        print("ERROR: No organizations found. Create one first.")
        sys.exit(1)
    org_id = org_result.data[0]["id"]
    print(f"Using organizacion_id: {org_id}")

    # Build category map
    cat_map = build_categoria_map(supabase)
    if not cat_map:
        print("ERROR: No categories found in categorias_tarea table. Run migrations first.")
        sys.exit(1)

    print(f"Found {len(cat_map)} categories: {list(cat_map.keys())}")
    print(f"Inserting {len(EXERCISES)} exercises...\n")

    inserted = 0
    skipped = 0

    for ex in EXERCISES:
        # Pop the helper field before building payload
        cat_code = ex.pop("categoria_codigo")
        cat_id = cat_map.get(cat_code)

        if not cat_id:
            print(f"  WARNING: Category '{cat_code}' not found in DB, skipping: {ex['titulo']}")
            skipped += 1
            # Restore so repeated runs work if needed (though idempotency handles this)
            ex["categoria_codigo"] = cat_code
            continue

        # Remove fields not in DB schema
        payload = {
            k: v for k, v in ex.items()
            if k not in FIELDS_TO_STRIP
        }
        payload.update({
            "categoria_id": cat_id,
            "organizacion_id": org_id,
            "es_plantilla": True,
            "es_publica": True,
            "tags": [SEED_TAG, cat_code.lower()],
        })

        try:
            result = supabase.table("tareas").insert(payload).execute()
            if result.data:
                inserted += 1
                print(f"  [{cat_code}] {ex['titulo']}")
            else:
                print(f"  WARNING: No data returned for '{ex['titulo']}'")
                skipped += 1
        except Exception as e:
            print(f"  ERROR inserting '{ex['titulo']}': {e}")
            skipped += 1
        finally:
            # Restore categoria_codigo in case dict is reused
            ex["categoria_codigo"] = cat_code

    print(f"\nDone! Inserted {inserted}/{len(EXERCISES)} exercises. Skipped: {skipped}.")


if __name__ == "__main__":
    seed()
